@push('scripts')
<script>
    var email = document.getElementById("email");

    email.addEventListener('invalid', handleEmailValidation, false);
    email.addEventListener('input', handleEmailValidation, false);

    function handleEmailValidation(event){
        if (email.value == '') {
            email.setCustomValidity('Bu alan zorunlu');
        }
        else if (email.validity.typeMismatch){
            email.setCustomValidity(email.value + ' geçerli bir e-posta adresi değil');
        }
        else {
           email.setCustomValidity('');
        }
        return true;
    }
</script>
